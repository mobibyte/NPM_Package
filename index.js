// Simple addition
function addThis(a, b) {
    var val1 = a;
    var val2 = b;

    return val1 + val2;
}

// Simple subtraction
function subtractThis(a, b) {
    var val1 = a;
    var val2 = b;

    return val1 - val2;
}

// Simple Multiplication
function multiplyThis(a, b) {
    var val1 = a;
    var val2 = b;

    return val1 * val2;
}

// Simple Division
function divideThis(a, b) {
    var val1 = a;
    var val2 = b;

    return val1 / val2;
}

module.exports = {addThis, subtractThis, multiplyThis, divideThis}
